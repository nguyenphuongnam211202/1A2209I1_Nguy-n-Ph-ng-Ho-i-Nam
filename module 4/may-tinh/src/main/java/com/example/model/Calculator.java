package com.example.model;

public class Calculator {

    public Calculator() {
    }

    public int cong(int a, int b){
        return a+b;
    }

    public int tru(int a, int b){
        return a - b;
    }

    public int nhan(int a, int b){
        return a*b;
    }

    public float chia(int a, int b){
        return (float) a /b;
    }
}
